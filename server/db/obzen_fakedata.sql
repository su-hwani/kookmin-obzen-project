USE obzen_test;

DROP TABLE IF EXISTS CHANNEL;	
DROP TABLE IF EXISTS CUSTOMER;
DROP TABLE IF EXISTS PRODUCT;
DROP TABLE IF EXISTS SELL_FINAL;

-------------------------------------------
-- Schema
-------------------------------------------
CREATE TABLE CHANNEL 
(
	STORE_NO 		varchar(10) NOT NULL COMMENT '판매점 코드' unique,
	STORE_NM 		varchar(50) DEFAULT NULL COMMENT '판매점명',
	CHANNEL_SECOND 	varchar(50) DEFAULT NULL COMMENT '채널(중)',
	STORE_STAT 		varchar(10) DEFAULT NULL COMMENT '판매점 거래 상태',
	STORE_DSTR 		varchar(50) DEFAULT NULL COMMENT '판매점 지역',
	CONSTRAINT 		PK_CHANNEL	PRIMARY KEY (STORE_NO)
)ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_general_ci;

CREATE TABLE CUSTOMER 
(
  CUST_NO 			varchar(20) NOT NULL COMMENT '고객번호' unique,
  AGE 				varchar(30) DEFAULT NULL COMMENT '연령',
  FRGN_CD_NM 		varchar(10) DEFAULT NULL COMMENT '내외국인',
  MNG_STORE_CD 		varchar(10) DEFAULT NULL COMMENT '관리점포코드',
  MNG_STORE_CD_NM 	varchar(50) DEFAULT NULL COMMENT '관리점포명',
  CHANNEL_SECOND 	varchar(20) DEFAULT NULL COMMENT '채널 중',
  GNDR_CD_NM 		varchar(10) DEFAULT NULL COMMENT '성별',
  BIRTH_YEAR 		varchar(4) DEFAULT NULL COMMENT '생년',
  BIRTH_DATE 		varchar(8) DEFAULT NULL COMMENT '생년월일',
  TM_YN 			varchar(1) DEFAULT NULL COMMENT 'TM 수신여부',
  SMS_YN 			varchar(1) DEFAULT NULL COMMENT 'SMS 수신여부',
  JOIN_DATE 		varchar(8) DEFAULT NULL COMMENT '가입일자',
  WITHDRAW_CD_NM 	varchar(10) DEFAULT NULL COMMENT '탈회여부',
  CUST_SEG_CD_NM 	varchar(10) DEFAULT NULL COMMENT '고객구분',
  CUST_GRD_CD_NM 	varchar(20) DEFAULT NULL COMMENT '고객등급(당해)',
  PRMTD_DATE 		varchar(8) DEFAULT NULL COMMENT '등급승급일',
  CUST_GRD_CD_NM_LAST varchar(20) DEFAULT NULL COMMENT '고객등급(전년)',
  SUBSC_PATH_CD_NM 	varchar(20) DEFAULT NULL COMMENT '가입경로코드명',
  POINT_BAL 		int(11) DEFAULT NULL COMMENT '잔여포인트',
  POINT_EXP 		int(11) DEFAULT NULL COMMENT '소멸예정포인트',
  POINT_BFH 		int(11) DEFAULT NULL COMMENT '보유선포인트',
  CONSTRAINT 		PK_CUSTOMER 			PRIMARY KEY (CUST_NO),
  CONSTRAINT 		FK_CUSTOMER_CHANNEL 	FOREIGN KEY (MNG_STORE_CD) 
										REFERENCES CHANNEL(STORE_NO)
                                    
									ON DELETE RESTRICT
									ON UPDATE CASCADE
)ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_general_ci;

CREATE TABLE PRODUCT 
(
	PRODUCT_NO 		varchar(10) 	NOT NULL COMMENT '제품 코드' unique,	
	PRODCUT_NM 		varchar(200) 	DEFAULT NULL COMMENT '제품명',
	PROD_CLASS_MAIN varchar(20) 	DEFAULT NULL COMMENT '제품 대분류',
	PRODUCT_BRAND 	varchar(10) 	DEFAULT NULL COMMENT '제품 브랜드',	
	PRODUCT_PRICE 	varchar(10) 	DEFAULT NULL COMMENT '소비자판매가',
	CONSTRAINT 		PK_PRODUCT		PRIMARY KEY (PRODUCT_NO)
)ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_general_ci;	

CREATE TABLE SELL_FINAL 
(	
	SELL_DATE 		date 			COMMENT '판매 년월일',	
	CUST_NO 		varchar(20) 	COMMENT '고객번호',	
	RCPT_BCD 		varchar(30) 	COMMENT '영수증바코드',	
	PRODUCT_NO 		varchar(20) 	COMMENT '제품 코드',	
	PRODUCT_NM 		varchar(100) 	DEFAULT NULL COMMENT '제품명',	
	CHANNEL_SECOND 	varchar(20) 	DEFAULT NULL COMMENT '채널 중',	
	STORE_NO 		varchar(10) 	COMMENT '판매점 코드',	
	STORE_NM 		varchar(50) 	DEFAULT NULL COMMENT '판매점명',	
	SALES_QTY 		int(11) 		DEFAULT NULL COMMENT '판매수량',	
	SALES_AMNT 		int(11) 		DEFAULT NULL COMMENT '판매금액 VAT 포함',	
	SALES_MRGN 		int(11) 		DEFAULT NULL COMMENT '판매순매출액 VAT 포함',
    CONSTRAINT		PK_SELL_FINAL	PRIMARY KEY (RCPT_BCD, SELL_DATE, CUST_NO, PRODUCT_NO),
	CONSTRAINT		FK_SELL_CUSTOMER FOREIGN KEY (CUST_NO) REFERENCES CUSTOMER(CUST_NO),
    CONSTRAINT 		FK_SELL_PROCUT_NO FOREIGN KEY (PRODUCT_NO) REFERENCES PRODUCT(PRODUCT_NO),
    CONSTRAINT 		FK_SELL_CHANNEL FOREIGN KEY (STORE_NO) REFERENCES CHANNEL(STORE_NO)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_general_ci;	

